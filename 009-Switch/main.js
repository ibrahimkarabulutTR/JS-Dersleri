let sonuc = 'aa';
// Kullanılacak değişken
switch(sonuc){
		// Koşul
		case 'aa':
				// Görev
				console.log("Notunuz aa");
				// Sonlandırma Komutu
				break;
		case 'ba':
				console.log("Notunuz b");
				break;
		case 'ab':
				console.log("Notunuz ab");
				break;
		case 'cb':
				console.log("Notunuz cb");
				break;
		case 'cc':
				console.log("Notunuz cc");
				break;
		// Varsayılan
		default:
				console.log("Geçemediniz");
				break;                                  
}